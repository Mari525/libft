#include "libft.h"

void		ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*a;

	if (lst && f)
	{
		a = lst;
		while (a)
		{
			f(a);
			a = a->next;
		}
	}
}
